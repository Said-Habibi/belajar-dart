void main() {
  // operator perbandingan adalah operator yang hasilnya adalah bool
  // operator perbandingan bisa digunakan di tipe data number dan string

  /*| Operator  | Keterangan |
    | :---      | :--- |
    | ==        | Sama dengan |
    | !=        | Tidak sama dengan |
    | >         | Lebih dari |
    | <         | Kurang dari |
    | >=        | Lebih dari atau sama dengan |
    | <=        | Kurang dari atau sama dengan |
*/

  // kode operator perbandingan
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print('said' == 'said');
  print('10' != '10');
}