void main() {
  // number
  // tipe data angka terdapat 2 tipe int dan double
  // int adalah bilangan bulat
  // double adalah bilangan desimal
  // penulisan koma dalam double menggunakan titik (format luar bukan indo ygy)

  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  // num
  // jika kita ingin menggunakan tipe data number yang bisa int ataupun double, kita bisa menggunakan tipe data num

  // kode num
  num number = 10;
  number = 10.5;
  print(number);
}